@section('scripts')
    <script>
        $(document).ready(function(){
            $('#dang-nhap').click(function(e){
                e.preventDefault();
                $.ajaxSetup({
                    headers: {
                        'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                    }
                });
                $.ajax({
                        'url' : 'login',
                        'data' : {
                            'email' : $('#email').val(),
                            'password' : $('#password').val()
                        },
                        'type' : 'POST',
                        success: function(data){
                            console.log(data);
                            if(data.error == true){
                                $('.error').hide();
                    
                            }
                            else{
                                window.location.href = "http://localhost:8000/employs"
                                }
                        }
                    });
            })
        });
    </script>
